const ALL_LOCALE_CODES = Object.keys(LOCALE_CONFIG);

function getLocaleConfig(lang) {
    return LOCALE_CONFIG[lang] ?? null;
}

function getOgLocale(lang) {
    return LOCALE_CONFIG[lang]?.ogLocale ?? 'en_US';
}

/** URL slug -> ISO 639-1 hreflang when they differ (e.g. /fil/ -> tl). */
const HREFLANG_BY_SLUG = {
    fil: 'tl'
};

function getHreflangCode(slug) {
    return HREFLANG_BY_SLUG[slug] ?? slug;
}

function getHtmlLang(lang) {
    return LOCALE_CONFIG[lang]?.htmlLang ?? lang;
}

function getHtmlDir(lang) {
    return LOCALE_CONFIG[lang]?.htmlDir ?? 'ltr';
}

function getFooterLastUpdatedConfig(lang) {
    return LOCALE_CONFIG[lang]?.footer ?? null;
}

function buildOgLocaleMap(languages) {
    return Object.fromEntries(languages.map((lang) => [lang, getOgLocale(lang)]));
}

module.exports = {
    LOCALE_CONFIG,
    ALL_LOCALE_CODES,
    getLocaleConfig,
    getHreflangCode,
    getOgLocale,
    getHtmlLang,
    getHtmlDir,
    getFooterLastUpdatedConfig,
    buildOgLocaleMap
};
